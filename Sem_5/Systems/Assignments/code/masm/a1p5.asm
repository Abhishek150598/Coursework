.MODEL SMALL
.STACK 100H

.DATA
	PROMPT1 DB "ENTER A CHARACTER $"
	PROMPT2 DB "PROGRAM TERMINATED $"

.CODE
	MAIN PROC
	
	MOV AX, @DATA
	MOV DS, AX

	LEA DX, PROMPT1  ; PRINT PROMPT1
	MOV AH, 09H
	INT 21H

	MOV AH, 01H ; READ A CHARACTER
	INT 21H

	MOV AH, 02H ; PRINTING NEWLINE
	MOV DL, 0DH
	INT 21H

	MOV AH, 02H
	MOV DL, 0AH
	INT 21H

	LEA DX, PROMPT2 ; PRINT TERMINATING MESSAGE
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H

	MAIN ENDP
	END MAIN
